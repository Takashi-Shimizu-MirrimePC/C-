//温度を摂氏から華氏に変換するプログラムを書け
//摂氏　＝　Celsius
//華氏　＝　Fahrenheit
//***F=(C*9.0/5.0)+32.0***
//***C=(F-32.0)*5.0/9.0***
#include <stdio.h>
 
int main ()
{
    printf("CELSIUS > FAHRENHEIT\n");
    float celsius,fahrenheit;
    int lower,upper,step;

    lower = 0 ;
    upper = 1000 ;
    step = 10;

    celsius = lower ;
    while( celsius <= upper ) {
        fahrenheit = celsius * (5.0/9.0) + 32.0;
        printf("%7.0f > %4.2f\n",celsius,fahrenheit);
        celsius = celsius + step;
    }
}
